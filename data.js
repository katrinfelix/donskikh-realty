function filterProperties(filters) {
  return properties.filter(p => {
    if (filters.type && filters.type !== 'all' && p.type !== filters.type) return false;
    if (filters.district && filters.district !== 'all' && p.district !== filters.district) return false;
    if (filters.priceMin && p.price < parseInt(filters.priceMin)) return false;
    if (filters.priceMax && p.price > parseInt(filters.priceMax)) return false;
    if (filters.areaMin && p.area < parseInt(filters.areaMin)) return false;
    if (filters.areaMax && p.area > parseInt(filters.areaMax)) return false;
    if (filters.rooms && filters.rooms !== 'all') {
      if (filters.rooms === '4' && p.rooms >= 4) return true;
      if (filters.rooms !== '4' && p.rooms !== parseInt(filters.rooms)) return false;
    }
    if (filters.search) {
      const s = filters.search.toLowerCase();
      const haystack = (p.title + ' ' + p.address + ' ' + p.districtName + ' ' + p.description).toLowerCase();
      if (!haystack.includes(s)) return false;
    }
    return true;
  });
}

function sortProperties(arr, sortBy) {
  const sorted = [...arr];
  switch (sortBy) {
    case 'price-asc': sorted.sort((a, b) => a.price - b.price); break;
    case 'price-desc': sorted.sort((a, b) => b.price - a.price); break;
    case 'area-asc': sorted.sort((a, b) => a.area - b.area); break;
    case 'area-desc': sorted.sort((a, b) => b.area - a.area); break;
    default: break;
  }
  return sorted;
}
