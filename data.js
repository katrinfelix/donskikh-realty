// Utility functions
function formatPrice(price) {
  return new Intl.NumberFormat('ru-RU').format(price) + '\u00A0\u20BD';
}

function formatPriceShort(price) {
  if (price >= 1000000) {
    const m = price / 1000000;
    return m % 1 === 0 ? m + '\u00A0млн\u00A0\u20BD' : m.toFixed(1) + '\u00A0млн\u00A0\u20BD';
  }
  return formatPrice(price);
}

